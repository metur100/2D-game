using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class OpenLink : MonoBehaviour
{
    public void Craftpix()
    {
        Application.OpenURL("https://craftpix.net/");
    }
    
    public void Freepik()
    {
        Application.OpenURL("https://www.freepik.com/upklyak");
    }
    public void PngTree()
    {
        Application.OpenURL("https://pngtree.com/");
    }
    public void UnityAssetStore()
    {
        Application.OpenURL("https://assetstore.unity.com/");
    }
    public void CreativeCommons()
    {
        Application.OpenURL("https://creativecommons.org/");
    }
}
